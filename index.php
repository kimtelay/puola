<p>Puola.fi portaalista löydät kaiken olennaisen Puolasta. Puola.fi on oikotiesi Puolaan.</p>

<p>
Puolan tasavallan kuuluu Itären rannikkovaltioihin. Sen väkiluku on 39 miljoonaa ja se on pinta-ala hieman Suomea pienempi. 
Pääkaupunki on Varsova. Puola on saanut nimensä varhaiskeskiajalla Puolassa asuneesta polaanien heimosta. Sana Polaani juontuu joko sanasta plemie (heimo) tai pole (pelto). Puola muodostui valtioksi 1000-luvulla.  Toisen maailmansodan aikana sai surmansa noin 4,3 miljoonaa puolalaista eli yli  12 % väestöstä.  Puolasta tuli NATOn jäsen vuonna 1999 ja se liittyi Euroopan unioniin vuonna 2004. 
</p>

